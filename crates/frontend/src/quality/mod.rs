//! # Фронтенд подсистемы контроля качества данных
//!
//! Таб `"quality_checks"` открывается из навигатора (блок «Контроль качества»).
//!
//! ## Структура
//!
//! ```text
//! quality/
//! └── ui/
//!     └── list/mod.rs   ← QualityCheckList — таблица проверок с кнопкой «Запустить»
//! ```
//!
//! ## UX
//!
//! Список проверок загружается при открытии таба (`GET /api/quality/checks`).
//! Кнопка «Запустить» отправляет `POST /api/quality/checks/{id}/run` и показывает
//! результат inline прямо в строке таблицы: итоговый счётчик и разбивку по проекциям.

