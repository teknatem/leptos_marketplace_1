-- p916: удалить устаревшие движения a040 из воронки.
--
-- Раньше a040 (поисковая аналитика) писал органические показы в show_free_count воронки.
-- Позже связь a040→p916 удалили: живой WB-эндпоинт /table/details отдаёт только `visibility`
-- (% показов в поиске), а не счётчик показов — смешивать % со штуками в show_free_count нельзя
-- (см. a040/llm.md, a040/repository.rs). Регистратор a040 больше НЕ трогает p916, поэтому старые
-- строки при перепроведении не удаляются и «висят» в проекции: колонка «Беспл.» дашборда d406
-- показывает эти орфанные значения, хотя органического источника нет.
--
-- Чистим их одноразово. После этого show_free_count в воронке становится NULL везде → «Беспл.»
-- корректно отображается как N/A (источник не подключён), а платные показы (a026) — как есть.

DELETE FROM p916_mp_sales_funnel_turnovers
WHERE registrator_type = 'a040_wb_search_analytics_daily';
